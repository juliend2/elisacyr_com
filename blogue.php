            <tr>
              <td class="no-wrap">
                9 h à 11 h
              </td>
              <td>
                Bloc 1 : Gestion des courriels et de l’agenda
              </td>
            </tr>

            <tr>
              <td class="no-wrap">
                11 h à 11 h 15
              </td>
              <td>Pause
              </td>
            </tr>

            <tr><td class="no-wrap">11 h 15 à 12 h 30</td><td>Bloc 2 : Rédaction d’infolettres
                et d’articles de blogue</td></tr>

          <tr>
            <td class="no-wrap">12 h 30 à 13 h </td><td>Pause dîner</td>
          </tr>

          <tr>
            <td class="no-wrap">13 h à 14 h </td><td>Bloc 3 : Réunions et suivis clients</td>
          </tr>

          <tr>
            <td class="no-wrap">14 h à 14 h 15 </td><td> Pause</td>
          </tr>

          <tr><td class="no-wrap">14 h 15 à 15 h 30 </td><td> Bloc 4 : Rédaction de rapports et
              tâches administratives. </td>
          </tr>
